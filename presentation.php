                <p>L'idée de l'A.S.E. est partie d'un constat fondé sur l'expérience de professionnels de la sommellerie. Ces derniers, tous investis depuis de longues années dans la restauration, l'hôtellerie, l'enseignement... oeuvrant à la vulgarisation de l'univers du vin, demeurent convaincus de l'absolue nécessité de s'adapter à l'évolution de l'économie vitivinicole. La Belle Époque de la France, comme celle où l'on faisait aveuglément confiance à " l'Art de vivre français ", est révolue ; se sont immiscés dans le creux de la vague, de nouvelles influences, émergeant des quatre points cardinaux du monde.</p>

                <p>Le challenge consiste donc à redorer le blason des cultures et des traditions européennes, en réfléchissant à de nouvelles stratégies de communication et de diffusion, correspondant davantage à la nouvelle donne de la mondialisation.</p>
                    
                <p>En préambule, l'A.S.E s'est fixée pour mission d'organiser et de favoriser les échanges inter-européens entre sommeliers, aux fins de confondre les méthodes de la profession et de les harmoniser. Il deviendra ainsi plus aisé de redéfinir les besoins et d'ouvrir de nouvelles orientations pour la formation, puis le perfectionnement des étudiants. Aujourd'hui encore, nombre de sommeliers étrangers viennent se former en France et les sommeliers français partent travailler hors de nos frontières.</p>
