                <p>Il était temps de repenser à l'organisation de l'enseignement et de l'apprentissage pour faciliter l'intégration des sommeliers et leur permettre d'accéder au rang de " Sommeliers d'Europe ".</p>

                <p>Son objectif prioritaire consistera à nouer des liens entre les écoles hôtelières des divers pays européens, à instituer des échanges entre les classes, puis à mettre en place un parrainage, en proposant aux sommeliers professionnels de devenir parrains ou référents, d'un ou de plusieurs élèves sommeliers, issus des territoires de la communauté.</p>
                    
                <p>En resserrant les liens entre vignerons européens et ainsi parer la poussée offensive des vins des autres continents (Amérique, Australie, Afrique et bientôt l'Asie (Chine)), l’A.S.E est déterminée à faire front commun pour la défense des différentes productions européennes. 
                    Le trait d'union entre le vigneron et le sommelier reste un gage de qualité et de bien boire. Renforcer les liens existants semble être, pour les membres de l'A.S.E, la condition sine qua non à la sauvegarde de nos vins.</p>

                   <p class="citation">" Boire du vin est aussi naturel que de manger, mais de surcroît, le vin est un dispensateur de plaisirs : plaisir sensoriel, plaisirs intellectuel et esthétique. "</p>
